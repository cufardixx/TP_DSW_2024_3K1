import mongoose from "mongoose";

// Crea un esquema para el usuario
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// Crea un modelo basado en el esquema
export default mongoose.model('User', userSchema);


